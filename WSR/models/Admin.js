const mongoose = require("mongoose");
const Schema = mongoose.Schema;

// create Admin Schema
const AdminSchema = new Schema({
  username: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

mongoose.model("admins", AdminSchema);
